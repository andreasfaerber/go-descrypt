func Encrypt(password, salt string) (string, error) {
	if len(salt) < SaltLen {
		return "", fmt.Errorf("salt must be at least %d characters", SaltLen)
	}
	for i := 0; i < SaltLen; i++ {
		if !isValidIto64(salt[i]) {
			return "", fmt.Errorf("invalid salt character at position %d", i)
		}
	}

	// Prepare key from password (up to 8 characters)
	var keyBuf [8]byte
	for i := 0; i < len(password) && i < 8; i++ {
		keyBuf[i] = byte(password[i]) & 0x7f
	}

	// Generate subkeys
	var keys [32]uint32
	pSetkey(keys[:], keyBuf[:])

	// Prepare salt values
	E := [2]uint32{
		uint32(saltTable[salt[0]]) << 8,
		uint32(saltTable[salt[1]]) << 4,
	}

	// Encrypt zero block
	var L, R uint32
	for i := 25; i > 0; i-- {
		key := 0
		for j := 7; j >= 0; j-- {
			T := R ^ (R >> 16)
			X := T & E[0]
			X ^= (X << 16) ^ R ^ keys[key]
			key++
			L ^= sp[6][X&0x3f]
			X >>= 8
			L ^= sp[4][X&0x3f]
			X >>= 8
			L ^= sp[2][X&0x3f]
			X >>= 8
			L ^= sp[0][X&0x3f]

			X = T & E[1]
			X ^= (X << 16) ^ R
			X = ((X << 4) | (X >> 28)) ^ keys[key]
			key++
			L ^= sp[7][X&0x3f]
			X >>= 8
			L ^= sp[5][X&0x3f]
			X >>= 8
			L ^= sp[3][X&0x3f]
			X >>= 8
			L ^= sp[1][X&0x3f]

			T = L ^ (L >> 16)
			X = T & E[0]
			X ^= (X << 16) ^ L ^ keys[key]
			key++
			R ^= sp[6][X&0x3f]
			X >>= 8
			R ^= sp[4][X&0x3f]
			X >>= 8
			R ^= sp[2][X&0x3f]
			X >>= 8
			R ^= sp[0][X&0x3f]

			X = T & E[1]
			X ^= (X << 16) ^ L
			X = ((X << 4) | (X >> 28)) ^ keys[key]
			key++
			R ^= sp[7][X&0x3f]
			X >>= 8
			R ^= sp[5][X&0x3f]
			X >>= 8
			R ^= sp[3][X&0x3f]
			X >>= 8
			R ^= sp[1][X&0x3f]
		}
		// Swap L and R
		L, R = R, L
	}

	// Final rotations
	L = (L << 3) | (L >> 29)
	R = (R << 3) | (R >> 29)

	// Final permutation (FP)
	exg2(&L, &R, 1, 0x55555555)
	exg2(&R, &L, 8, 0xff00ff)
	exg2(&R, &L, 2, 0x33333333)
	exg2(&L, &R, 16, 0xffff)
	exg2(&L, &R, 4, 0x0f0f0f0f)

	// Encode result
	var result [14]byte
	copy(result[:2], salt[:2])
	p := 13
	result[p] = 0
	p--
	result[p] = out[(R<<2)&0x3f]
	R >>= 4
	p--
	result[p] = out[R&0x3f]
	R >>= 6
	p--
	result[p] = out[R&0x3f]
	R >>= 6
	p--
	result[p] = out[R&0x3f]
	R >>= 6
	p--
	result[p] = out[R&0x3f]
	R >>= 6
	p--
	result[p] = out[((L<<4)|R)&0x3f]
	L >>= 2
	p--
	result[p] = out[L&0x3f]
	L >>= 6
	p--
	result[p] = out[L&0x3f]
	L >>= 6
	p--
	result[p] = out[L&0x3f]
	L >>= 6
	p--
	result[p] = out[L&0x3f]
	L >>= 6
	p--
	result[p] = out[L&0x3f]

	return string(result[:13]), nil
}

func Verify(password, hash string) bool {
	if len(hash) < HashLen {
		return false
	}
	salt := hash[:SaltLen]
	expected := hash[SaltLen:]
	computed, err := Encrypt(password, salt)
	if err != nil {
		return false
	}
	return computed[SaltLen:] == expected
}

// pSetkey prepares a key for encryption/decryption (PC-1 + PC-2)
func pSetkey(keys []uint32, k []byte) {
	var L, R, A, B uint32

	// Load key bytes with bit shifting (multiply by 2)
	L = uint32(k[0]<<1) << 24
	L |= uint32(k[1]<<1) << 16
	L |= uint32(k[2]<<1) << 8
	L |= uint32(k[3] << 1)
	R = uint32(k[4]<<1) << 24
	R |= uint32(k[5]<<1) << 16
	R |= uint32(k[6]<<1) << 8
	R |= uint32(k[7] << 1)

	// PC-1 permutation using exchange operations
	exg2(&R, &L, 4, 0x0f0f0f0f)
	exg1(&L, 14, 0xcccc)
	exg1(&L, 7, 0xaa00aa)
	exg1(&L, 4, 0x0f0f0f0f)
	exg1(&L, 2, 0x33333333)
	exg1(&L, 1, 0x55555555)
	exg1(&R, 18, 0x3333)
	exg1(&R, 9, 0x550055)
	exg1(&R, 4, 0x0f0f0f0f)
	R = (R << 4) | (L & 0xf)
	L >>= 4

	// PC-2 and generate 16 subkeys (each is 2 uint32s = 32 total)
	// C iterates i=15..0 and stores sequentially (*keys++ = A; *keys++ = B)
	// We iterate 0..15 to match C's sequential key order
	keyIdx := 0
	for i := 0; i < 16; i++ {
		if rot[i] == 0 {
			L = (L<<1 | L>>27) & 0x0fffffff
			R = (R<<1 | R>>27) & 0x0fffffff
		} else {
			L = (L<<2 | L>>26) & 0x0fffffff
			R = (R<<2 | R>>26) & 0x0fffffff
		}

		A = pc2[0][L>>24] |
			pc2[1][(L>>20)&0xf] |
			pc2[2][(L>>16)&0xf] |
			pc2[3][(L>>12)&0xf] |
			pc2[4][(L>>8)&0xf] |
			pc2[5][(L>>4)&0xf] |
			pc2[6][L&0xf]
		B = pc2[7][R>>24] |
			pc2[8][(R>>20)&0xf] |
			pc2[9][(R>>16)&0xf] |
			pc2[10][(R>>12)&0xf] |
			pc2[11][(R>>8)&0xf] |
			pc2[12][(R>>4)&0xf] |
			pc2[13][R&0xf]

		exg2(&B, &A, 16, 0x3f3f)
		keys[keyIdx] = A
		keys[keyIdx+1] = B
		keyIdx += 2
	}
}

// exg1: exchange bits within a single value
func exg1(a *uint32, n uint32, m uint32) {
	t := ((*a >> n) ^ *a) & m
	*a ^= t
	*a ^= t << n
}

// exg2: exchange bits between two values
func exg2(a, b *uint32, n uint32, m uint32) {
	t := (((*a) >> n) ^ *b) & m
	*b ^= t
	*a ^= t << n
}

func isValidIto64(b byte) bool {
	return indexOfIto64(b) >= 0
}

func indexOfIto64(b byte) int {
	for i := 0; i < len(ito64); i++ {
		if byte(ito64[i]) == b {
			return i
		}
	}
	return -1
}

func GenerateSalt() (string, error) {
	b := make([]byte, SaltLen)
	if _, err := rand.Read(b); err != nil {
		return "", err
	}
	for i := range b {
		b[i] = ito64[int(b[i])%len(ito64)]
	}
	return string(b), nil
}
